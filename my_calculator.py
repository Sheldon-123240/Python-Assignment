#Welcome to my calculator

#We will ask the user to input the first number
num_1 = float(input("What's the first number?"))

#We will ask the user to input the second number
num_2 = float(input("What's the second number?"))

sum_result = num_1 + num_2
print(f"Sum: {sum_result}")

difference_result = num_1 - num_2
print(f"difference: {difference_result}")

product_result = num_1 * num_2
print(f"product: {product_result}")

quotient_result = num_1 // num_2
print(f"quotient: {quotient_result}")
